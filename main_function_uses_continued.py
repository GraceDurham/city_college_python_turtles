


def squareit(num):
    """ return square of number"""

    return num * num


def cubeit(num):
    """ return cube of a number"""

    return num * num * num 

def main():
    
    anum = int(input("Please enter a number"))
    print(squareit(anum))
    print(cubeit(anum))

if __name__ == '__main__':
    main()










